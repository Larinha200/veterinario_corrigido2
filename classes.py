class Gato :
    def __init__ (self, Nome, Idade, Raca, Cor, Peso):
        self.__Nome__ = Nome 
        self.__Idade__= Idade
        self.__Raca__= Raca
        self.__Cor__= Cor
        self.__Peso__= Peso

    def getNome(self):
        return self.__Nome__
    
    def getIdade(self):
        return self.__Idade__
    
    def getRaca(self):
        return self.__Raca__
    
    def getCor(self):
        return self.__Cor__
    
    def getPeso(self):
        return self.__Peso__
    

