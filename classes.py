class Gato :
    def __init__ (self, Nome, Idade, Raca, Cor, Peso):
        self.__Nome = Nome 
        self.__Idade= Idade
        self.__Raca= Raca
        self.__Cor= Cor
        self.__Peso= Peso

    def getNome(self):
        return self.__Nome
    
    def getIdade(self):
        return self.__Idade
    
    def getRaca(self):
        return self.__Raca
    
    def getCor(self):
        return self.__Cor
    
    def getPeso(self):
        return self.__Peso
    

